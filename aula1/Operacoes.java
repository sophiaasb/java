public class Operacoes {
    
    public static void main(String[] args) {
        
        double a = 6;
        double b = 5;
        double soma = a+b;
        double sub = a-b;
        double multi = a*b;
        double div = a/b;

        System.out.println("Soma: " +soma);
        System.out.println("Subtração: " +sub);
        System.out.println("Multiplicação: " +multi);
        System.out.println("Divisão: " +div);

    }
}
